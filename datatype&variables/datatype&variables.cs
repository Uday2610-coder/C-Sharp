using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace datatype_variables
{
    internal class datatype_variables
    {
        static void Main(string[] args)
        {
            // data type

            int a = 32;
            float b = 32.2F;
            double c = 32.2D;
            bool d = false;
            char e = 'a';
            string s = "This is a string";

            System.Console.WriteLine(a);
            System.Console.WriteLine(b);
            System.Console.WriteLine(c);
            System.Console.WriteLine(d);
            System.Console.WriteLine(e);
            System.Console.WriteLine(s);


            // variable

            string name = "Uday";
            System.Console.WriteLine(name);

            int myNum = 15;
            System.Console.WriteLine(myNum);

            char myLetter = 'D';
            System.Console.WriteLine(myLetter);

            bool myBool = false;
            System.Console.WriteLine(myBool);

            double myDouble = 5.88D;
            System.Console.WriteLine(myDouble);
        }

    }

}

